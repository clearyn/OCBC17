//Contoh Iterasi
console.log("//Contoh Iterasi while : Mulai");
var i = 0;
while (i <= 5) {
    console.log(i);
    i++;
}
console.log("//Contoh Iterasi while : Selesai");

console.log("\n//Contoh Iterasi for : Mulai");
for (let j = 0; j <= 3; j++) {
    console.log(j);
}
console.log("//Contoh Iterasi for : Selesai");